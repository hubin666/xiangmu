	//先获取轮播图的数据
				$.get("json/lunbo.json", function(data){					
					var arr = data;
					
					for (var i=0; i<arr.length; i++) {
						var obj = arr[i];
						
						$("<li><img src="+ obj.img +" ></li>").appendTo("#ul1");
						var li = $("<li>"+ (i+1) +"</li>").appendTo("#ul2");
						
						if (i==0) {
							li.addClass("active");
						}
					}
					
					//轮播
					lunbo();
				})
				
				//jq轮播图
				function lunbo(){
					
					var ul1 = $("#ul1");
					var ul2 = $("#ul2");
					var li1 = $("#ul1 li");
					var li2 = $("#ul2 li");
					
					//复制第一张图到最后
					li1.first().clone(true).appendTo(ul1);
					
					//
					var size = $("#ul1 li").size();
					ul1.width(1200*size);
					
					//开启定时器
					var i = 0;
					var timer = setInterval(function(){
						i++;
						move();
					}, 3000);
					
					function move(){
						
						if (i < 0) {
							ul1.css("left", -1200*(size-1));
							i = size-2;
						}
						
						if (i >= size){
							ul1.css("left", 0);
							i = 1;
						}						
						ul1.stop().animate({left:-i*1200}, 1800);
						
						li2.eq(i).addClass("active").siblings().removeClass("active");
						if (i == size-1) {
							li2.eq(0).addClass("active").siblings().removeClass("active");
						}
					}
										
					li2.mouseenter(function(){
						i = $(this).index();
						move();
					})
					
					$("#box2").hover(function(){
						clearInterval(timer);
					}, 
					function(){
						timer = setInterval(function(){
							i++;
							move();
						}, 3000);
					})
				}
	
